    public void tampil() {

        EntityManager em = Persistence.createEntityManagerFactory("PBO13PU").createEntityManager();

        Query q = em.createNamedQuery("Matakuliah.findAll");
        List<Matakuliah> hasil = q.getResultList();

        DefaultTableModel tblmk = new DefaultTableModel(new String[]{"KODE MK", "SKS", "NAMA MK", "SEMESTERAJAR"}, 0);
        for (Matakuliah data : hasil) {
            Object[] baris = new Object[4];
            baris[0] = data.getKodemk();
            baris[1] = data.getSks();
            baris[2] = data.getNamamk();
            baris[3] = data.getSemesterajar();
            tblmk.addRow(baris);
        }
        table.setModel(tblmk);

    }

