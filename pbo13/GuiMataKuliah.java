    private void btnUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadActionPerformed
        // TODO add your handling code here:
        JFileChooser jfc = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
        int returnValue = jfc.showOpenDialog(null);

        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File filePilihan = jfc.getSelectedFile();
            System.out.println("File yang dipilih: " + filePilihan.getAbsolutePath());

            try (BufferedReader br = new BufferedReader(new FileReader(filePilihan))) {
                String baris;
                String pemisah = ",";

                EntityManagerFactory emf = Persistence.createEntityManagerFactory("PBO13PU");
                EntityManager em = emf.createEntityManager();

                em.getTransaction().begin();

                while ((baris = br.readLine()) != null) {
                    String[] matakuliah = baris.split(pemisah);

                    if (matakuliah.length == 4) {
                        String kodemk = matakuliah[0];
                        String sks = matakuliah[1];
                        String namamk = matakuliah[2];
                        String semesterajar = matakuliah[3];

                        if (!kodemk.isEmpty() && !sks.isEmpty() && !namamk.isEmpty() && !semesterajar.isEmpty()) {
                            try {
                                Matakuliah mk = new Matakuliah();
                                mk.setKodemk(kodemk);
                                mk.setSks(Integer.parseInt(sks));
                                mk.setNamamk(namamk);
                                mk.setSemesterajar(Integer.parseInt(semesterajar));
                                em.persist(mk);
                            } catch (Exception e) {
                                JOptionPane.showMessageDialog(null, "Data Gagal Disimpan: " + e.getMessage());
                            }
                        }
                    }
                }

                em.getTransaction().commit();
                JOptionPane.showMessageDialog(null, "Data Berhasil Diunggah");
                em.close();
                emf.close();

            } catch (FileNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "File tidak ditemukan: " + ex.getMessage());
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, "Error Membaca File: " + ex.getMessage());
            }
        }

        tampil();

    }//GEN-LAST:event_btnUploadActionPerformed

